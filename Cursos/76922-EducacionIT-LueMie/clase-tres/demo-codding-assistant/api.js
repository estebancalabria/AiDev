//Una api rest en node.js con express que maneje una lista de tareas (to do list) con las siguientes funcionalidades:
// Crear una tarea (POST /tasks)
// Obtener todas las tareas (GET /tasks)
// Obtener una tarea por id (GET /tasks/:id)
// Actualizar una tarea (PUT /tasks/:id)
// Eliminar una tarea (DELETE /tasks/:id)
// La tarea debe tener un id, un titulo, una descripcion y un estado (pendiente, en progreso, completada)